num1 = int(input("Enter a number: "))
operation = input("Enter one of these operations /,*,+,-: ")
num2 = int(input("Enter a number: "))
if operation == '+':
    print(num1+num2)
elif operation == '*':
    print(num1*num2)
elif operation == '/':
    print(num1/num2)
elif operation=='-':
    print(num1-num2)


